# ─── 格式工具 ──────────────────────────────────────────────────

FONT_CN = "宋体"
FONT_EN = "Times New Roman"
FONT_SIZE = Pt(12)
TITLE_SIZE = Pt(16)


def _create_doc():
    doc = Document()
    style = doc.styles["Normal"]
    style.font.name = FONT_EN
    style.font.size = FONT_SIZE
    style._element.rPr.rFonts.set("{http://schemas.openxmlformats.org/wordprocessingml/2006/main}eastAsia", FONT_CN)
    return doc


def _add_title(doc, title, is_final=False):
    prefix = "期末考试试题" if is_final else "章末习题"
    p = doc.add_paragraph()
    p.alignment = WD_ALIGN_PARAGRAPH.CENTER
    run = p.add_run(f"{title} — {prefix}")
    run.bold = True
    run.font.size = TITLE_SIZE
    doc.add_paragraph()


def _add_heading(doc, text):
    p = doc.add_paragraph()
    run = p.add_run(text)
    run.bold = True
    run.font.size = Pt(14)
    doc.add_paragraph()


def _add_q(doc, text):
    p = doc.add_paragraph()
    run = p.add_run(text)
    run.font.size = FONT_SIZE


def _save(doc, path):
    path = Path(path)
    path.parent.mkdir(parents=True, exist_ok=True)
    doc.save(str(path))
    print(f"  已生成: {path}")
    return path



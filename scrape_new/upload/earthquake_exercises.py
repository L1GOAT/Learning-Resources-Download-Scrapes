def generate_chapter_exercise(ch_index: int, ch_name: str, output_path: str):
    """生成单章习题 docx"""
    if ch_index not in BANK:
        print(f"  [跳过] 第{ch_index}章 无题库（专题/纪录片章节）")
        return None

    clean = ch_name
    bank = BANK[ch_index]
    single, multi, judge = bank[0], bank[1], bank[2]

    doc = _create_doc()
    _add_title(doc, clean)
    _add_heading(doc, "一、单项选择题")
    for i, q in enumerate(single, 1):
        _add_q(doc, f"{i}. {q['s']}（{q['a']}）")
        for o in q["o"]:
            _add_q(doc, o)
        _add_q(doc, f"[解析]：{q['r']}")
        doc.add_paragraph()

    _add_heading(doc, "二、多项选择题")
    for i, q in enumerate(multi, 1):
        _add_q(doc, f"{i}. {q['s']}（{q['a']}）")
        for o in q["o"]:
            _add_q(doc, o)
        _add_q(doc, f"[解析]：{q['r']}")
        doc.add_paragraph()

    _add_heading(doc, "三、判断题")
    for i, q in enumerate(judge, 1):
        s = q["s"]
        remark = q.get("r", "")
        _add_q(doc, f"{i}. {s}")
        if remark:
            _add_q(doc, f"[解析]：{remark}")
        doc.add_paragraph()

    return _save(doc, output_path)


